def get_has_evidence_edges(firs, registry) -> list:
    """Build HAS_EVIDENCE edges: Crime node -> Evidence nodes."""
    edges = []
    for fir in firs:
        cm_id = registry.get_case_master_id(fir.fir_id)
        if cm_id is None:
            continue
        for ev_type in fir.identifiers_mentioned.get("accounts", []):
            edges.append({
                ":START_ID":    cm_id,
                ":END_ID":      ev_type,
                ":TYPE":        "HAS_EVIDENCE",
                "source_fir_id": cm_id,
            })
    return edges


def get_charged_under_edges(firs, fir_projections, registry) -> list:
    """Build CHARGED_UNDER edges: Crime node -> Section nodes."""
    edges = []
    for fir in firs:
        cm_id = registry.get_case_master_id(fir.fir_id)
        if cm_id is None:
            continue
        proj = fir_projections.get(fir.fir_id, {})
        for asa in proj.get("act_section_assocs", []):
            section_id_str = f"{asa['ActCode']}_{asa['SectionCode']}"
            # Map to legal_layer section_id format (ITACT->IT)
            act_map = {"ITACT": "IT", "BNS": "BNS", "PMLA": "PMLA",
                       "BSA": "BSA", "BNSS": "BNSS"}
            prefix = act_map.get(asa["ActCode"], asa["ActCode"])
            section_node_id = f"{prefix}_{asa['SectionCode']}"
            edges.append({
                ":START_ID":    cm_id,
                ":END_ID":      section_node_id,
                ":TYPE":        "CHARGED_UNDER",
                "source_fir_id": cm_id,
            })
    return edges
