def reset_asa_counter() -> None:
    global _asa_counter
    _asa_counter = 0

def build_act_section_associations(
    case_master_id: int,
    crime_type: str,
    extra_sections: list[tuple[str, str]] | None = None,
) -> list[KSPActSectionAssociation]:
    """
    Build KSPActSectionAssociation rows for a case.

    Args:
        case_master_id: INT PK of the CaseMaster row.
        crime_type: e.g. 'digital_arrest', 'task_scam', etc.
        extra_sections: optional additional (act_code, section_code) pairs
                        beyond the canonical list for this crime_type.

    Returns:
        List of KSPActSectionAssociation dataclass instances.
    """
    global _asa_counter
    canonical = sections_for_crime_type(crime_type)
    all_sections: list[tuple[str, str]] = list(canonical)
    if extra_sections:
        for pair in extra_sections:
            if pair not in all_sections:
                all_sections.append(pair)

    rows: list[KSPActSectionAssociation] = []
    for act_code, section_code in all_sections:
        # Validate against KSP masters
        if act_code not in km.ACT_MAP:
            continue
        if section_code not in km.SECTION_MAP:
            continue
        _asa_counter += 1
        rows.append(KSPActSectionAssociation(
            asa_id         = _asa_counter,
            case_master_id = case_master_id,
            act_code       = act_code,
            section_code   = section_code,
        ))
    return rows


def get_section_label(act_code: str, section_code: str) -> str:
    """Return a human-readable label like 'IT Act S.66C'."""
    act_names = {
        "ITACT": "IT Act",
        "BNS":   "BNS",
        "PMLA":  "PMLA",
        "BSA":   "BSA",
        "BNSS":  "BNSS",
    }
    return f"{act_names.get(act_code, act_code)} S.{section_code}"


def sections_label_list(crime_type: str) -> str:
    """Return a comma-separated string of section labels for a crime type."""
    pairs = sections_for_crime_type(crime_type)
    return ", ".join(get_section_label(a, s) for a, s in pairs)


